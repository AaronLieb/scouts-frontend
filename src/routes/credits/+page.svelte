	<h2>Game Design</h2>
	<p>
		Most of the fundamental rules of Scouts were created by alicevoid and myself.
		<br /><br />It was all created over the course of one night, with rapid prototypes using cutout
		pieces of paper. We started with no rules, and just started moving pieces around, and making up
		random rules and seeing if they felt right. We came up with a cohesive set of rules pretty
		quickly, and we were surprised with how it felt like a real game despite just coming up with it.
		<br /><br />Huge thanks to alicevoid!
		<br /><br />After the initial rules were set, I created a simple website to play the game, and
		then played it with a few of my friends.
		<br /><br />I might be forgetting some names, but shoutouts to Vicente Huerta, Charlie Taylor,
		and Justin Stitt for being some of the early playtesters!
	</p>

	<h2>Physical Edition</h2>
	<p>
		In preparation for the competition that would be featuring Scouts, I needed four physical boards
		for scouts. I needed someone who was a woodworker, so I reached out to my good friend, Jozef
		Chavez.
		<br /><br />Jozef and I, with some help from his father, Hector Chavez, created the first four
		wooden boards of Scouts.
	</p>

	<h2>Website Development</h2>
	<p>
		After I showed Scouts to my friends Justin and diamondburned, they both went and just started a
		coding up a backend and frontend for the game. Diamond worked on a go backend, with a wasm
		module for porting over the game logic to be used on the frontend. Justin and I worked on the
		Svelte frontend.
		<br /><br />After some short initial progress, nearly a year passed without any additional work
