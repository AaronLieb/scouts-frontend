<main>
	<h1>Rules</h1>

	<h2>Requirements</h2>
	<ul>
		<li>An 8x10 board</li>
		<li>5 flippable black pieces</li>
		<li>5 flippable white pieces</li>
		<li>two rocks that take up a 2x2 on the board (boulders)</li>
		<li>Two rocks that take up a 2x1 on the board</li>
		<li>A chess timer (optional)</li>
	</ul>
	<h2>Overview</h2>
	<p>
		Scouts is a game about gathering intel using movement and positioning. Pieces cannot take or
		kill each other, they are scouts, meant to get to the enemy's side, and get back with the intel.
		Each player has 5 pieces, they will first engage in a setup period, then the game begins by
		players taking turns playing pieces. Once a piece gets to the enemy side, it flips, signifying
		that it has gathered the intel. Once a player gets a flipped piece back to their side, that
		player has won. There are several movement options during each play, as well as a one-time
		playable boulder that each player may use during one of their turns.
	</p>
	<h2>Setup</h2>
	<ul>
		<li>
			Face the shorter side of the board towards the two players, and place the boulders to the side
			of the board.
		</li>
		<li>
			Both players alternate placing one of their pieces on their first row. White starts first, and
			setup ends once both players have placed all 5 pieces. Place the pieces white/black side up.
		</li>
		<li>The game begins and the chess timer starts with white going first.</li>
	</ul>
	<h2>Turns and Plays</h2>
	<p>
		Each turn is made up of two (2) plays. Each play is made up of 1+ moves.
		<br /><br />
		There is one exception to this rule, which is white's first turn of the game, where they are only
		allowed to use one (1) play on that turn. A different piece must be played in each play. A player
		cannot play the same piece twice in a turn.Â 
		<br /><br />
		Turn > play > move
	</p>
	<h2>Moves</h2>
	<p>There are two types of moves:</p>

	<h3>Dash</h3>
	<ul>
		<li>Move a piece to any of the 8 adjacent squares (orthogonal + diagonal)</li>
		<li>A dash always ends the play. The piece cannot move again until the next turn</li>
	</ul>
	<h3>Jump</h3>
	<ul>
		<li>Move a piece two spaces in any of the 4 orthogonal directions by jumping over a piece.</li>
		<li>A piece cannot jump diagonally</li>
		<li>A piece can jump over enemy pieces and ally pieces</li>
		<li>The piece must be jumping into an empty space</li>
		<li>A piece cannot jump over stacked pieces or boulders</li>
		<li>After jumping, the play continues, and the piece may move again (or choose not to).</li>
		<li>
			A piece can jump multiple times in one play as long as there are pieces to keep jumping over.
		</li>
	</ul>
	<h3>Boulders</h3>
	<ul>
		<li>Each player starts with one 2x2 boulder and one 2x1 boulder</li>
		<li>
			A player can place a boulder at any point during their turn, and it does not consume a play,
			or count as a move.
		</li>
		<li>
			The boulder must be placed on an empty area and cannot touch any rocks on any of the cardinal
			directions.
		</li>
	</ul>
	<h2>Gathering Intel</h2>
	<p>
		Once a player's piece has reached the enemy's back rank, the piece is flipped to signify that it
		has gathered the intel. Flipping a piece always ends that play. Even if the piece jumped onto
		the enemy's back rank, it cannot move again afterwards. Multiple pieces can gather intel.
	</p>
	<h2>Winning</h2>
	<p>
		Once a player gets a flipped piece back to their starting row, that player has won the game.
		Alternatively, if a player runs out of time on their timer, they lose the game.
	</p>
	<h2>Reminders / Common Mistakes</h2>
	<ul>
		<li>A piece cannot jump diagonally</li>
		<li>A piece cannot dash, then jump</li>
		<li>A player cannot use both plays on the same piece in a turn</li>
		<li>Flipping a piece ends the play</li>
		<li>A piece cannot jump between stacks, but can dash between stacks</li>
		<li>White only has 1 play on their first turn</li>
		<li>Make sure each player presses the switch on the chess timer after they end their turn</li>
	</ul>
